import java.util.List;

public class ListPair {
    private List<Object> list1;
    private List<Object> list2;

    public ListPair(List<Object> list1, List<Object> list2) {
        this.list1 = list1;
        this.list2 = list2;
    }

    public List<Object> getList1() {
        return list1;
    }

    public List<Object> getList2() {
        return list2;
    }
}
